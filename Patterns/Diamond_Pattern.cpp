#include <iostream>
using namespace std;

int main()
{
    int n;

    cout << "Enter n: ";
    cin >> n;

    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= n - i; j++) // Spaces
        {
            cout << " ";
        }
        for (int j = 1; j <= 2 * i - 1; j++)
        {
            cout << "*"; // Stars
        }
        cout << endl;
    }
    for (int i = n; i >= 1; i--)
    {
        for (int j = 1; j <= n - i; j++) // Spaces
        {
            cout << " ";
        }
        for (int j = 1; j <= 2 * i - 1; j++) // Stars
        {
            cout << "*";
        }

        cout << endl;
    }
    return 0;
}